// Remplacer l'ancien dictionnaire par le nouveau
export { extendedIngredientTranslations as ingredientTranslations };

// Fonction pour traduire un ingrédient
export const translateIngredient = (ingredient: string, targetLanguage: Language): string => {
  // Si la langue cible est le français, retourner tel quel
  if (targetLanguage === 'fr') {
    return ingredient;
  }
  
  // Normaliser l'ingrédient (minuscules, trim, supprimer accents)
  const normalizedIngredient = ingredient.toLowerCase().trim()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, ''); // Supprimer les accents
  
  // Chercher la traduction avec plusieurs variantes
  let translation = extendedIngredientTranslations[targetLanguage]?.[normalizedIngredient];
  
  // Si pas trouvé, essayer sans accents dans le dictionnaire
  if (!translation) {
    const normalizedDict = Object.keys(extendedIngredientTranslations[targetLanguage] || {});
    const matchingKey = normalizedDict.find(key => 
      key.normalize('NFD').replace(/[\u0300-\u036f]/g, '') === normalizedIngredient
    );
    if (matchingKey) {
      translation = extendedIngredientTranslations[targetLanguage]?.[matchingKey];
    }
  }
  
  // Si toujours pas trouvé, essayer des variantes communes
  if (!translation) {
    // Variantes pour "oeuf/œuf"
    if (normalizedIngredient.includes('oeuf') || normalizedIngredient.includes('œuf')) {
      if (normalizedIngredient.endsWith('s')) {
        translation = extendedIngredientTranslations[targetLanguage]?.['œufs'] || 
                     extendedIngredientTranslations[targetLanguage]?.['oeufs'];
      } else {
        translation = extendedIngredientTranslations[targetLanguage]?.['œufs']?.slice(0, -1) || // Enlever le 's'
                     extendedIngredientTranslations[targetLanguage]?.['oeufs']?.slice(0, -1);
      }
    }
    
    // Variantes pour "sucre glacé/sucre glace"
    if (normalizedIngredient.includes('sucre glace') || normalizedIngredient.includes('sucre glace')) {
      translation = extendedIngredientTranslations[targetLanguage]?.['sucre glacé'] || 
                   extendedIngredientTranslations[targetLanguage]?.['sucre glace'];
    }
  }
  
  // Retourner la traduction ou l'original si pas trouvé
  return translation || ingredient;
};